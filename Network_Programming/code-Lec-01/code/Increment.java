public class Increment {
		public static void main( String[] args ) {
 
		 int c;
		 c = 5; // assign 5 to c
		 System.out.println( c ); //  prints 
		 System.out.println( c++ ); //  prints  
		 System.out.println( c ); //  prints 
		 System.out.println(); // skip a line

		 // demonstrate prefix increment operator
		 c = 5; // assign 5 to c
		 System.out.println( c ); //  prints 
		 System.out.println( ++c ); //   
		 System.out.println( c ); //  prints 
		} // end main
	} // end class Increment

